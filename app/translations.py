def get_translator(lang='en'):
    """Return a translation function for the given language."""
    def t(key):
        if lang == 'en' or lang not in ('ta', 'hi', 'te'):
            return key
        entry = TRANSLATIONS.get(key)
        if entry and lang in entry:
            return entry[lang]
        return key
    return t
